class Complaint {
  final String photo;
  final String name;
  final String zone;
  final String complaintType;
  final String date;
  final String status;
  final String uid;
  final String complaintId;

  Complaint(
      {required this.photo,
      required this.name,
      required this.zone,
      required this.complaintType,
      required this.date,
      required this.status,
      required this.uid,
      required this.complaintId});
}
